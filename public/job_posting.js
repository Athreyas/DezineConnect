                $(document).ready(function() {
		$('#box2').addClass("jobposting_set1");
       		$('#box2 textarea, input').focus(function() {
       			$('#box2').removeClass("jobposting_set1").addClass("jobposting_set1_on");
                        $("#box2 .box2").show();

                       // $('#box1').tipsy({gravity: 'w'});
                       // $(input[type="text"].tipsy({gravity: 'w'});
    		    if (this.value == this.defaultValue){
    		    	this.value = '';
				}
				if(this.value != this.defaultValue){
	    			this.select();
	    		}
    		});
    		$('#box2 textarea, input').blur(function() {
    			$('#box2').removeClass("jobposting_set1_on").addClass("jobposting_set1");
                        $("#box2 .box2").hide();
    		    if ($.trim(this.value) == ''){
			    	this.value = (this.defaultValue ? this.defaultValue : '');
				}
    		});
		});






